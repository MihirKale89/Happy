class ModTester{
  public static void main(String args[]){
    System.out.println("The mod of 12 and 5 is "+12%5);
    System.out.println("The mod of -12 and 5 is "+(-12%5));
    System.out.println("The mod of 12 and -5 is "+(12%-5));
    System.out.println("The mod of -12 and -5 is "+(-12%-5));
  }
}
